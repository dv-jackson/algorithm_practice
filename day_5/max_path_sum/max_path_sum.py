# 🌳 Day 5 Problem: Binary Tree Maximum Path Sum (Hard)
# Given the root of a binary tree, return the maximum path sum.
# 📌 Definition:
# A path is any sequence of nodes where each pair of adjacent nodes has an edge connecting them.
# A node can only appear once in the path.
# The path does NOT need to pass through the root.
# 🧾 Example:
#        -10
#        /  \
#       9   20
#          /  \
#         15   7
# Output: 42
# 👉 Path: 15 → 20 → 7
# 🧠 Function Signature:
# def maxPathSum(root) -> int:
#     pass
# ⚙️ Constraints:
# Nodes can have negative values
# Must consider all possible paths, not just root-to-leaf
# Time complexity target: O(n)